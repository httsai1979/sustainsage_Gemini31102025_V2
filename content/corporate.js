export function resolveLocale(locale) {
  if (!locale) {
    return FALLBACK_LOCALE;
  }

  const normalized = locale.toLowerCase();

  if (normalized.startsWith('zh-cn') || normalized === 'zh' || normalized.includes('hans')) {
    return 'zh-CN';
  }

  if (normalized.startsWith('zh-tw') || normalized.includes('hant')) {
    return 'zh-TW';
  }

  return 'en-GB';
}

export function getCorporateCopy(locale) {
  const key = resolveLocale(locale);
  return CORPORATE_CONTENT[key] ?? CORPORATE_CONTENT[FALLBACK_LOCALE];
}

export function getLayerBySlug(locale, layerSlug) {
  const copy = getCorporateCopy(locale);
  if (!layerSlug) return null;
  return copy.layers.find((layer) => layer.slug === layerSlug) ?? null;
}

export function getSubLayerBySlugs(locale, layerSlug, subLayerSlug) {
  const layer = getLayerBySlug(locale, layerSlug);
  if (!layer) return { layer: null, subLayer: null };

  const subLayer = layer.subLayers.find((item) => item.slug === subLayerSlug) ?? null;
  return { layer, subLayer };
}

export function getAllLayerSlugs() {
  return Object.entries(CORPORATE_CONTENT).flatMap(([locale, copy]) =>
    (copy.layers || []).map((layer) => ({ locale, layerSlug: layer.slug }))
  );
}

export function getAllSubLayerSlugs() {
  return Object.entries(CORPORATE_CONTENT).flatMap(([locale, copy]) =>
    (copy.layers || []).flatMap((layer) =>
      (layer.subLayers || []).map((subLayer) => ({ locale, layerSlug: layer.slug, subLayerSlug: subLayer.slug }))
    )
  );
}
